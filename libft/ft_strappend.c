#include "libft.h"

void	ft_strappend(char **s1, char *s2)
{
	char	*tmp;

	if (!*s1 || !s2)
		return ;
	tmp = ft_strjoin(*s1, s2);
	ft_strdel(s1);
	*s1 = tmp;
}
